
marks = int(input("Enter yours marks\n"))
if marks >= 100:
    print("Yours grade is Ex-A")
elif marks > 80:
    print("Yours grade is A")
elif marks > 70:
    print("Yours grade is B")
elif marks > 60:
    print("Yours grade is C")
elif marks > 50:
    print("Yours grade is d")
else:
    print("You are fail")
